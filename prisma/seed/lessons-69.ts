`# Работа с треками в DAW

## Типы треков

В DAW существует несколько принципиально разных типов дорожек. Понимание их назначения — основа грамотного routing'а.

## Audio Track (Аудио-трек)

**Audio Track** воспроизводит и записывает реальный звук — аудиофайлы (WAV, AIFF, MP3).

| Характеристика | Описание |
|--------------|---------|
| Содержимое | Аудиофайлы на диске |
| Редактирование | Неразрушающее (оригинал не меняется) |
| Нагрузка | Дисковая (I/O), умеренная CPU |
| Применение | Гитара, вокал, барабаны, любой живой звук |

Аудио-треки **привязаны к временной шкале**: сдвиг региона меняет его позицию во времени. При записи DAW создаёт аудиофайл на диске и помещает его на трек как регион.

**Моно vs Стерео:**
- Моно-трек — один канал (вокал, гитара, бас)
- Стерео-трек — два канала L+R (синтезаторы, обработанный сигнал, мастер)

## MIDI Track

**MIDI Track** хранит не звук, а **инструкции для воспроизведения**: какая нота, когда, с какой силой.

| Характеристика | Описание |
|--------------|---------|
| Содержимое | MIDI-события (ноты, CC, aftertouch) |
| Звук | Генерируется внешним MIDI-устройством |
| Редактирование | Гибкое (изменение нот, transpose, квантайз) |
| Применение | Управление внешними синтезаторами, хардверными модулями |

MIDI-трек **сам по себе не звучит** — ему нужен получатель: внешний синтезатор через MIDI-интерфейс или виртуальный инструмент.

## Instrument Track (Инструментальный трек)

**Instrument Track** = MIDI-трек + встроенный виртуальный инструмент (VSTi/AU).

Это самый распространённый тип трека в современном продюсировании:

\`\`\`
MIDI Piano Roll → [Виртуальный инструмент (Kontakt, Serum, etc.)] → Аудиосигнал → Микшер
\`\`\`

| Характеристика | Описание |
|--------------|---------|
| Гибкость | Можно менять ноты даже после записи |
| CPU | Высокая (просчёт синтеза в реальном времени) |
| Применение | Синтезаторы, виртуальные барабаны, сэмплеры |

**Замораживание (Freeze)** — функция, которая рендерит инструментальный трек в аудио для экономии CPU, сохраняя возможность «разморозить» и отредактировать MIDI.

## Bus / Aux Track (Шина)

**Bus** — технический трек для маршрутизации сигналов.

### Параллельная обработка (Parallel Processing)

\`\`\`
Drum 1 (Kick)  ─┐
Drum 2 (Snare) ─┤→ [Drum Bus] → [Компрессор на шине] → Мастер
Drum 3 (OH)    ─┘
\`\`\`

Вместо того чтобы вешать компрессор на каждый барабанный трек, можно направить все барабаны в одну шину и обработать их вместе. Это:
- Экономит CPU
- Позволяет обработать группу как единое целое
- Даёт «склейку» звука

### Send / Return (Посыл / Возврат)

\`\`\`
Guitar ─→ [Основной сигнал] → Микшер
        ↓
       [Send (10%)] → [Reverb Aux] → Микшер (параллельно)
\`\`\`

Reverb на возврате (Return/Aux track) — стандартная практика:
- Один ревербератор обрабатывает много треков
- Экономия ресурсов CPU
- Единое акустическое пространство для всех инструментов

## Group Track (Группа)

**Group** аналогичен Bus, но обычно используется для управления громкостью и обработки взаимосвязанных треков как единого целого.

| Типичные группы | Содержимое |
|----------------|-----------|
| Drums Group | Kick, Snare, Toms, Overheads, Room |
| Guitar Group | DI, Cab, Room mic |
| Vocal Group | Lead vocal, backing vocals |
| Mix Bus | Все инструменты перед мастером |

## Master Track (Мастер)

**Master Track** — финальная точка сбора всего звука. Всё, что на нём, влияет на общий выходной сигнал. Здесь размещаются:
- Лимитер (предотвращение клиппинга)
- Мастеринговый EQ
- Анализатор спектра

## Routing (Маршрутизация)

**Routing** — назначение входов и выходов треков.

\`\`\`
[Микрофон (вход интерфейса)] → Input в DAW
                                    ↓
                           [Audio Track 1]
                                    ↓
                           [Drum Bus]
                                    ↓
                           [Master]
                                    ↓
                    [Выход интерфейса → Мониторы/Наушники]
\`\`\`

### Мониторинг при записи

- **Input Monitoring (Direct Monitoring)** — слышите себя через интерфейс без задержки
- **Software Monitoring** — слышите себя через DAW (может быть задержка/latency)
- **Low Latency Mode** — снижение буфера для минимальной задержки

> **Золотое правило routing'а:** всегда понимайте, куда идёт каждый сигнал. Сложные маршруты красиво решают задачи, но легко запутать себя. Используйте цветовое кодирование треков и понятные названия.`

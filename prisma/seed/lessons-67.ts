`# Протокол MIDI

## Что такое MIDI

**MIDI** (Musical Instrument Digital Interface) — стандарт передачи музыкальных данных между электронными устройствами, разработанный в 1983 году. Это не звук — это **управляющие сообщения**: нажать клавишу, отпустить, изменить параметр.

MIDI-сигнал сообщает: «нажата нота C4 с силой удара 90» — но не как она должна звучать. Звучание определяет принимающее устройство (синтезатор, модуль, плагин).

## Физический интерфейс MIDI

### DIN 5-pin (классический MIDI)

Круглый 5-штырьковый разъём DIN 41524.

- **MIDI OUT** — передача данных с устройства
- **MIDI IN** — приём данных
- **MIDI THRU** — сквозная передача входящих данных (цепочка устройств)

Скорость: **31250 бод** (бит/с) — фиксированная, неизменная с 1983 года.

Задержка: при последовательной передаче ноты — около 1 мс. При большом количестве одновременных сообщений — возможна небольшая задержка (MIDI congestion).

### MIDI over USB

Современный стандарт — передача MIDI по USB-кабелю. Преимущества:
- Питание по тому же кабелю (для некоторых устройств)
- Большая пропускная способность (несколько MIDI-портов в одном USB-соединении)
- Простое подключение к компьютеру без дополнительных интерфейсов

### MIDI over Ethernet / RTP-MIDI

Передача MIDI по сети (Wi-Fi или Ethernet). Используется в сложных сценических установках и студиях.

## Структура MIDI-сообщений

Каждое MIDI-сообщение — это 1–3 байта данных.

### Note On / Note Off

**Note On** — сообщение о нажатии клавиши:
\`\`\`
[Status byte: 0x9n] [Note number: 0–127] [Velocity: 0–127]
n = MIDI-канал (0–15)
\`\`\`

**Velocity (сила удара):** 0 = беззвучно, 64 = среднее, 127 = максимум. Velocity = 0 эквивалентно Note Off.

**Note Off** — сообщение об отпускании клавиши:
\`\`\`
[Status byte: 0x8n] [Note number] [Release velocity]
\`\`\`

### Номера нот (Note Numbers)

Стандарт GM: **C4 (middle C) = нота 60**. Диапазон 0–127 охватывает более 10 октав.

| Нота | Номер |
|------|-------|
| C4 (middle C) | 60 |
| A4 (440 Гц) | 69 |
| C5 | 72 |
| C3 | 48 |

### Control Change (CC)

**CC** — сообщение об изменении контроллера (колесо модуляции, педали, любые параметры):

\`\`\`
[Status: 0xBn] [Controller number: 0–127] [Value: 0–127]
\`\`\`

**Стандартные CC-номера:**

| CC# | Функция |
|-----|---------|
| 1 | Modulation Wheel (колесо модуляции) |
| 7 | Volume |
| 10 | Pan (панорама) |
| 11 | Expression |
| 64 | Sustain Pedal (on/off) |
| 91 | Reverb Send |
| 93 | Chorus Send |

### Program Change (PC)

**Program Change** — смена пресета/программы:
\`\`\`
[Status: 0xCn] [Program number: 0–127]
\`\`\`

В General MIDI (GM) — стандартные номера инструментов (1 = Acoustic Grand Piano, 41 = Violin и т.д.).

### Pitch Bend

**Pitch Bend** — изменение высоты тона (аналог колеса питча):
\`\`\`
[Status: 0xEn] [LSB] [MSB] — 14-битное разрешение (16384 шага)
\`\`\`

14-битное разрешение даёт плавное, без ступенчатости изменение высоты тона.

### Aftertouch

**Aftertouch (Channel Pressure)** — давление на уже нажатую клавишу. Используется для vibrato, expression, открытия фильтра. Поддерживается не всеми клавиатурами.

## MIDI-каналы

MIDI поддерживает **16 независимых каналов** по одному кабелю. Каждый канал может управлять отдельным инструментом или тембром.

**General MIDI:** канал 10 зарезервирован для ударных инструментов.

## SysEx (System Exclusive)

**SysEx** — специальные сообщения, уникальные для каждого производителя. Используются для:
- Передачи пресетов и настроек
- Обновления прошивки
- Управления параметрами, которых нет в стандартном CC

## Ограничения классического MIDI и MPE

**MPE (MIDI Polyphonic Expression)** — расширение протокола, в котором каждой ноте назначается собственный канал. Это позволяет управлять pitch bend, pressure и timbre независимо для каждой ноты — основа для инструментов типа Roli Seaboard и Roger Linn LinnStrument.

> **Практически:** MIDI — это универсальный язык электронных музыкальных инструментов. Понимание даже базовых сообщений (Note On/Off, CC, Program Change) позволяет строить сложные автоматизированные системы, связывать несколько синтезаторов, контроллеров и программного обеспечения.`
